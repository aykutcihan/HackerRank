# Warmup 03 — Define a Function
# Goal: Define a function that takes two parameters and returns their sum.
#
# Instructions:
# Define a function called add(a, b) that returns a + b.
# Then call it with two hardcoded values (e.g. 4 and 6) and print the result.
# Do NOT use input() yet — use hardcoded values.
#
# Example:
#   add(4, 6) → prints 10

def add(a, b):
    total = a + b
    return total
print(add(4, 6))